    <h1>SU DI NOI</h1>
    <p>Il team "Gli strattoniani" si sono occupati nella realizzazione di un sito web di annunci, "Presto.it"
    Esso è basato su:
        multi-merceologico e multi lingua (inglese, Francese e Spagnolo) prevedendo soluzioni responsive, implementando due modalità di visualizzazione light-mode e dark-mode.
    Il sito è stato ideato mobile-first.
    "Presto.it"
        prevede all'utente non registrato di visualizzare gli articoli e i propri dettagli di qualsiasi categoria,
        una volta registratosi potrà publicare annunci e modificare
        gli stessi in un secondo momento; potrà inoltre richiedere l'autorizzazione all'admin per diventare revisore tramite una richiesta che verrà autamoticamente compilata e mandata tramite e-mail direttamente all'admin il quale potrà decidere se accettare o rifiutare la richiesta  potendo moderare gli annunci inseriti dagli utenti,
    Collaborando in questa maniera con lo staff del sito.
    Per la realizzazzione abbiamo utilizzato vari linguaggi e librerie, nello specifico
    parte front-end ( HTML; CSS; Bootsrap; Javascript; swiperJS)
    parte beck-end (Laravel; insieme ai suoi componenti come Livewire e PHP)
    e per il team working (Git)
    Come si può vedere dal sito presenta una grafica lineare e pulita che rende semplice ed intuitiva l'esperienza del nostro utente medio.
    Distinti saluti "GLI STRATTONIANI"</p>
